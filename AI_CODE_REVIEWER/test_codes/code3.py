def decision(x):
    if x == 1:
        return "a"
    elif x == 2:
        return "b"
    elif x == 3:
        return "c"
    elif x == 4:
        return "d"
    elif x == 5:
        return "e"
    elif x == 6:
        return "f"
    elif x == 7:
        return "g"
    elif x == 8:
        return "h"
    elif x == 9:
        return "i"
    else:
        return "z"
